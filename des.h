void DES_Encrypt(char *str, char *hex) { //str：明文，hex：密文
	Des_Run(str, str, ENCRYPT);
	cout<<"加密结果为：";
	str2hex(hex,str,64);
	int n=strlen(hex);
	for(int i=0; i<n; ++i) {
		cout<<hex[i];
	}
	cout<<endl<<endl;
}

